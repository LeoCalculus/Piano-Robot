// Low-level SPI helpers - keep CS low during multi-byte sequences
static void lcd_cmd(uint8_t cmd){
    CMD_MODE();
    HAL_SPI_Transmit(&hspi1, &cmd, 1, 10);
}

static void lcd_data(uint8_t data){
    DATA_MODE();
    HAL_SPI_Transmit(&hspi1, &data, 1, 10);
}

static void lcd_data_multi(uint8_t *data, uint16_t len){
    DATA_MODE();
    HAL_SPI_Transmit(&hspi1, data, len, 100);
}

void LcdWriteCmd(uint8_t cmd){
    SELECT_LCD();
    lcd_cmd(cmd);
    UNSELECT_LCD();
}

void LcdWriteData(uint8_t data){
    SELECT_LCD();
    lcd_data(data);
    UNSELECT_LCD();
}

void LcdInit(void){
    // hardware reset sequence (active low)
    LCD_RST_HIGH();
    HAL_Delay(5);
    LCD_RST_LOW();
    HAL_Delay(20);
    LCD_RST_HIGH();
    HAL_Delay(150);

    SELECT_LCD();

    // software reset
    lcd_cmd(0x01);
    UNSELECT_LCD();
    HAL_Delay(150);
    SELECT_LCD();

    // display off
    lcd_cmd(0x28);

    // power control 1
    lcd_cmd(0xC0);
    lcd_data(0x23);

    // power control 2
    lcd_cmd(0xC1);
    lcd_data(0x10);

    // VCOM control 1
    lcd_cmd(0xC5);
    lcd_data(0x3E);
    lcd_data(0x28);

    // VCOM control 2
    lcd_cmd(0xC7);
    lcd_data(0x86);

    // memory access control - 0x08 for portrait, BGR
    lcd_cmd(0x36);
    lcd_data(0x08);

    // pixel format 16-bit
    lcd_cmd(0x3A);
    lcd_data(0x55);

    // frame rate control
    lcd_cmd(0xB1);
    lcd_data(0x00);
    lcd_data(0x18);

    // display function control
    lcd_cmd(0xB6);
    lcd_data(0x08);
    lcd_data(0x82);
    lcd_data(0x27);

    // sleep out
    lcd_cmd(0x11);
    UNSELECT_LCD();
    HAL_Delay(120);

    SELECT_LCD();
    // display on
    lcd_cmd(0x29);
    UNSELECT_LCD();
    HAL_Delay(50);

    // clear screen to black
    LcdFillScreen(COLOR_BLACK);
}

void LcdSetWindow(uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1){
    SELECT_LCD();

    // column address set
    lcd_cmd(0x2A);
    uint8_t colData[4] = {x0 >> 8, x0 & 0xFF, x1 >> 8, x1 & 0xFF};
    lcd_data_multi(colData, 4);

    // row address set
    lcd_cmd(0x2B);
    uint8_t rowData[4] = {y0 >> 8, y0 & 0xFF, y1 >> 8, y1 & 0xFF};
    lcd_data_multi(rowData, 4);

    // memory write command
    lcd_cmd(0x2C);
    // NOTE: CS stays LOW, caller sends pixel data then calls UNSELECT_LCD()
}

void LcdFillScreen(uint16_t color){
    LcdSetWindow(0, 0, LCD_WIDTH - 1, LCD_HEIGHT - 1);
    // CS is already LOW from LcdSetWindow

    uint8_t data[2] = {color >> 8, color & 0xFF};

    DATA_MODE();
    // send pixel by pixel 
    for(uint32_t i = 0; i < (uint32_t)LCD_WIDTH * LCD_HEIGHT; i++){
        HAL_SPI_Transmit(&hspi1, data, 2, HAL_MAX_DELAY);
    }
    UNSELECT_LCD();
}

void LcdDrawPixel(uint16_t x, uint16_t y, uint16_t color){
    if(x >= LCD_WIDTH || y >= LCD_HEIGHT) return;

    LcdSetWindow(x, y, x, y);
    // CS is LOW from LcdSetWindow
    uint8_t data[2] = {color >> 8, color & 0xFF};
    lcd_data_multi(data, 2);
    UNSELECT_LCD();
}

void LcdDrawChar(uint16_t x, uint16_t y, char c, uint16_t color, uint16_t bg){
    if(c < 32 || c > 126) c = '?';

    const uint8_t *glyph = font5x7[c - 32];

    // draw character using a window for better performance
    LcdSetWindow(x, y, x + 5, y + 6);
    DATA_MODE();

    uint8_t colorHi = color >> 8;
    uint8_t colorLo = color & 0xFF;
    uint8_t bgHi = bg >> 8;
    uint8_t bgLo = bg & 0xFF;

    // font is 5 cols x 7 rows, plus 1 col spacing = 6x7 total
    for(uint8_t row = 0; row < 7; row++){
        for(uint8_t col = 0; col < 6; col++){
            uint8_t pixel[2];
            if(col < 5 && (glyph[col] & (1 << row))){ // c5 used for char spacing, according to char bit map do plot pixel
                pixel[0] = colorHi;
                pixel[1] = colorLo;
            } else {
                pixel[0] = bgHi;
                pixel[1] = bgLo;
            }
            HAL_SPI_Transmit(&hspi1, pixel, 2, 10);
        }
    }
    UNSELECT_LCD();
}

void LcdDrawString(uint16_t x, uint16_t y, const char *str, uint16_t color, uint16_t bg){
    while(*str){
        if(x + 6 > LCD_WIDTH){
            x = 0;
            y += 8;
        }
        if(y + 7 > LCD_HEIGHT) return;

        LcdDrawChar(x, y, *str, color, bg);
        x += 6;  // 5 pixels + 1 spacing
        str++;
    }
}

// cursor position
static uint16_t cursor_x = 0;
static uint16_t cursor_y = 0;

void LcdSetCursor(uint16_t x, uint16_t y){
    cursor_x = x;
    cursor_y = y;
}

void LcdPrint(const char *str, uint16_t color, uint16_t bg){
    while(*str){
        if(cursor_x + 6 > LCD_WIDTH){
            cursor_x = 0;
            cursor_y += 8;
        }
        if(cursor_y + 7 > LCD_HEIGHT){
            cursor_y = 0;  // wrap to top
        }
        LcdDrawChar(cursor_x, cursor_y, *str, color, bg);
        cursor_x += 6;
        str++;
    }
}
